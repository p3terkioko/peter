"use client";

import ErrorDisplay from "@/components/ErrorDisplay";
import { useEffect } from "react";

export default function Error({
  error,
  reset,
}: {
  error: Error & { digest?: string };
  reset: () => void;
}) {
  useEffect(() => {
    console.error(error);
  }, [error]);

  return <ErrorDisplay code={500} message="Internal Server Error" />;
}
